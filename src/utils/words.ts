export function getAllCategories(): string[] {
    return Object.keys(WORD_CATEGORIES);
}

export function getRandomWord(selectedCategories: string[] = []): string {
    const activeCategories = selectedCategories.length > 0 
        ? selectedCategories 
        : getAllCategories();

    const allAvailableWords = activeCategories.flatMap(cat => WORD_CATEGORIES[cat] || []);
    
    if (allAvailableWords.length === 0) return 'IMPOSTOR'; // Fallback
    
    return allAvailableWords[Math.floor(Math.random() * allAvailableWords.length)];
}

export function getRandomImpostorIndices(playerCount: number, impostorCount: number): number[] {
    const indices: number[] = [];
    while (indices.length < impostorCount) {
        const randomIndex = Math.floor(Math.random() * playerCount);
        if (!indices.includes(randomIndex)) {
            indices.push(randomIndex);
        }
    }
    return indices;
}

